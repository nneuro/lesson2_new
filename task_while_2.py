dict_ask_question = {
    '1': 'Хорошо!',
    'Что делаешь?' : 'Программирую',
    'Кто тебя придумал?' : 'Я сам себя придумал'
}

def ask_user():
    while True:
        user_say = input('Программа: спроси меня что нибудь или скажи "Пока"\nПользователь: ')
        if user_say == dict_ask_question.keys():
            print(dict_ask_question.values())
        elif user_say == 'Пока':
            break
    return('До встречи!')

ask=ask_user()
